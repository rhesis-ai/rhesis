"""Shared lightweight reference schemas for *Detail response schemas.

Every *Detail schema across the app that needs a
first-level relationship expanded to more than just its id reuses these
instead of redefining an identical class per model.

Only include a class here if its shape is genuinely identical across every
consumer. Some models need a richer version of a reference (e.g. TestRun's
TestSetReference nests test_set_type) -- those stay local to their own
schema module rather than being forced into a shared shape.
"""

from typing import Any, Dict, List, Optional

from pydantic import UUID4, ConfigDict

from rhesis.backend.app.schemas import Base
from rhesis.backend.app.schemas.tag import TagRead


class StatusReference(Base):
    id: UUID4
    name: Optional[str] = None
    description: Optional[str] = None
    user_id: Optional[UUID4] = None
    organization_id: Optional[UUID4] = None

    model_config = ConfigDict(from_attributes=True)


class ProjectReference(Base):
    id: UUID4
    name: Optional[str] = None
    description: Optional[str] = None
    user_id: Optional[UUID4] = None
    organization_id: Optional[UUID4] = None
    status_id: Optional[UUID4] = None
    attributes: Optional[Dict[str, Any]] = None
    tags: Optional[List[TagRead]] = None
    icon: Optional[str] = None

    model_config = ConfigDict(from_attributes=True)


